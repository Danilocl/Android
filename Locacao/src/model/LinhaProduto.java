package model;

public class LinhaProduto{

	private int id;
	private String nome;
	private String marcaLnP;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getMarcaLnP() {
		return marcaLnP;
	}

	public void setMarcaLnP(String marcaLnP) {
		this.marcaLnP = marcaLnP;
	}
	
}
